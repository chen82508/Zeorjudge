/*********************************************************************
 *
 * 任選兩條垂直的直線，搭配任選兩條水平的直線，就可以形成一個矩形
 *
 *********************************************************************/

#include <iostream>

using namespace std ;

int main () {
    long long int n, m ;

    while ( cin >> n >> m )
        cout << ( ( n * ( n + 1 ) ) >> 1 ) * ( ( m * ( m + 1 ) ) >> 1 ) << endl ;

    return 0 ;
}
